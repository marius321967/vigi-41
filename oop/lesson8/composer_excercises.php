1.1
Inicializuokite nauja composer projekta l6/1_inventory_checker.php uzduoties kataloge.
Projekto pavadinimas 'jusu-vardas-pavarde/inventory-checker'

1.2
Nustatykite susiekite savo projekto namespace su src/ direktorija

1.3
Pakeiskite savo rašytą autoloaderį Composer'io autoloaderiu

1.4 Įsirašykite Monolog loggerį (https://packagist.org/packages/monolog/monolog) ir naudokite jį log'inimui vietoje savo
pačių rašyto kodo

1.5
Įsidiekite 'php-cs-fixer' įrankį į savo projekto dev dependencies. Paleiskite įrankį ir sutvarkykite klaidas, kurias jis rado

https://github.com/FriendsOfPHP/PHP-CS-Fixer

1.6 Ištrinkite vendor/ direktorija ir vėl iš naujo suinstaliuokite visus dependencius

